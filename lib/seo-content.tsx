/**
 * مكون لعرض محتوى غني بالكلمات المفتاحية
 * @param type نوع الصفحة
 * @param customTitle عنوان مخصص (اختياري)
 * @param customIntro نص مقدمة مخصص (اختياري)
 */
interface RichContentProps {
  type: PageType;
  customTitle?: string;
  customIntro?: string;
  hideCallToAction?: boolean;
  className?: string;
}

export const RichContent: React.FC<RichContentProps> = ({
  type,
  customTitle,
  customIntro,
  hideCallToAction = false,
  className,
}) => {
  // اختيار المحتوى المناسب حسب نوع الصفحة
  let content: SEOPageContent;
  
  switch (type) {
    case PageType.HOME:
      content = homeContent;
      break;
    case PageType.SERVICES:
      content = servicesContent;
      break;
    case PageType.SERVICE_DETAIL:
      // يمكن تحديد نوع الخدمة هنا
      content = valetParkingContent;
      break;
    default:
      content = homeContent;
  }
  
  return (
    <div className={className}>
      <h2 className="text-2xl font-bold mb-4">
        {customTitle || content.h1Title}
      </h2>
      
      <div className="mb-6 text-lg">
        {customIntro || content.introText}
      </div>
      
      <div className="space-y-6">
        {content.paragraphs.map((paragraph, index) => (
          <div key={index}>
            <h3 className="text-xl font-semibold mb-2">{paragraph.title}</h3>
            <p className="text-gray-700">{paragraph.content}</p>
          </div>
        ))}
      </div>
      
      {!hideCallToAction && (
        <div className="mt-8 p-4 bg-beige-light rounded-lg">
          <p className="font-bold text-center text-brown-dark">{content.callToAction}</p>
        </div>
      )}
    </div>
  );
};

/**
 * مكون المفاتيح الدلالية للصفحة
 * @param type نوع الصفحة
 */
export const KeywordsList: React.FC<{ type: PageType; className?: string }> = ({ type, className }) => {
  // اختيار المحتوى المناسب حسب نوع الصفحة
  let content: SEOPageContent;
  
  switch (type) {
    case PageType.HOME:
      content = homeContent;
      break;
    case PageType.SERVICES:
      content = servicesContent;
      break;
    case PageType.SERVICE_DETAIL:
      content = valetParkingContent;
      break;
    default:
      content = homeContent;
  }
  
  // دمج الكلمات المفتاحية
  const allKeywords = [...content.primaryKeywords, ...content.secondaryKeywords];
  
  return (
    <div className={className}>
      <div className="flex flex-wrap gap-2">
        {allKeywords.map((keyword, index) => (
          <span 
            key={index} 
            className="bg-beige-light text-brown-dark px-3 py-1 text-sm rounded-full"
          >
            {keyword}
          </span>
        ))}
      </div>
    </div>
  );
};

